public class SESubItem {
    private String id;
    private String name;
    private long time;
    private String cid;
    private long outtime;
    private String date;
    private String txt;
    private String icon;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public long getTime() {
        return time;
    }

    public String getCid() {
        return cid;
    }

    public long getOuttime() {
        return outtime;
    }

    public String getDate() {
        return date;
    }

    public String getTxt() {
        return txt;
    }

    public String getIcon() {
        return icon;
    }
}
